#!/usr/bin/env python3
from __future__ import annotations

import argparse
import csv
from pathlib import Path
from typing import List

import matplotlib.pyplot as plt


def load_rows(path: Path) -> List[dict]:
    with path.open("r", encoding="utf-8") as f:
        reader = csv.DictReader(f)
        rows = list(reader)
    if not rows:
        raise SystemExit(f"No rows found in {path}")
    return rows


def main() -> None:
    parser = argparse.ArgumentParser(description="Plot layer-wise compression ratios from summarize_layer_compression CSV")
    parser.add_argument("--csv", type=Path, required=True, help="CSV file generated by summarize_layer_compression.py")
    parser.add_argument("--png", type=Path, required=True, help="Output PNG path")
    parser.add_argument("--title", default="Layer-wise Compression", help="Plot title")
    args = parser.parse_args()

    rows = load_rows(args.csv)
    layers = [int(row["layer"]) for row in rows]
    direct_ratio = [float(row["direct_ratio"]) for row in rows]
    split_ratio = [float(row["split_ratio"]) for row in rows]
    improvement = [-float(row["delta_pct_vs_direct"]) for row in rows]

    fig, axes = plt.subplots(2, 1, figsize=(11, 8), sharex=True, gridspec_kw={"height_ratios": [2.0, 1.2]})

    ax_top, ax_bottom = axes
    ax_top.plot(layers, direct_ratio, marker="o", linewidth=2.0, color="#1f4e79", label="Direct Zstd Ratio")
    ax_top.plot(layers, split_ratio, marker="s", linewidth=2.0, color="#b85c38", label="Stride-Reorg Ratio")
    ax_top.set_ylabel("Compressed / Raw")
    ax_top.grid(True, alpha=0.25)
    ax_top.legend(frameon=False, loc="best")

    ax_bottom.bar(layers, improvement, color="#5f8f3a", width=0.7)
    ax_bottom.set_ylabel("Extra Gain (%)")
    ax_bottom.set_xlabel("Layer")
    ax_bottom.grid(True, axis="y", alpha=0.25)

    fig.suptitle(args.title)
    fig.tight_layout()
    args.png.parent.mkdir(parents=True, exist_ok=True)
    fig.savefig(args.png, dpi=180, bbox_inches="tight")
    print(f"Saved plot: {args.png}")


if __name__ == "__main__":
    main()
